  EXPECT_EQ(11, text_samples_[1]->settings().line.value().value);
  EXPECT_EQ("ttx_11", text_samples_[1]->settings().region);
  EXPECT_EQ(TextAlignment::kCenter,
            text_samples_[1]->settings().text_alignment);
